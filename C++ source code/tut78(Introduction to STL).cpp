/*
containers -> Container library is a collection of classes
The containers are implemented as genric class templates
Examples of containers are stacks, linked list, queue, array, associative array, vector etc...

Algorithms -> They provide abstraction i.e. we do not need to write the complex algoritms on our own rather STL provides the algoritms for us for example sorting algorithms primarily Introsort which is one of the best sorting algoritms in the world and is comprised of quick sort, heap sort, insertion sort.

Iterators -> They act as a bridge between containers and algoritms i.e. we access the data in the containers and then apply the algoritms this is only possible by iterators as they help us to access the elements of the containers and apply certain algoritms.
*/
